//for - quando sabemos o inicio e o fim

// let i = 0 é igual ao inicio, i <= 10 e o fim, logo temos a definição do innicio e do fim 
for (let i = 0; i <= 10; i++){
    console.log(i);
    
}

//while - usar quando nao sabemos nem o inicio nem o fim 

let contador = 0

while (contador <= 5) {
    console.log(contador);
    contador++
}

