//VAR e LET

//Declarando VAR, VAR sempre é uma vartiavel global
var texto = "Hello World"
console.log(texto);
//console.log == print()

//Declarando LET, LET é sempre uma variavel local
let nome = "Vitor Alexandre Lusni"
console.log("nome: ", nome);


//Atribuindo um novo valor
nome = "Pedro"
console.log("nome: ", nome);

//Atribuindo um novo valor
texto = "Olá, mundo"
console.log(texto);

//Constante - const
const valorPI = Math.PI
console.log("O valor de PI é igual a ", valorPI)

